import SwiftUI

struct ContentView: View {
    var emojis = ["ğŸš²", "ğŸš‚", "ğŸš", "ğŸšœ", "ğŸš•", "ğŸï¸", "ğŸš‘", "ğŸš“", "ğŸš’", "âœˆï¸", "ğŸš€", "â›µï¸", "ğŸ›¸", "ğŸ›¶", "ğŸšŒ", "ğŸï¸", "ğŸ›º", "ğŸš ", "ğŸ›µ", "ğŸš—", "ğŸšš", "ğŸš‡", "ğŸ›»", "ğŸš"]
    
    @State var emojiCount = 20
    
    var body: some View {
        VStack {
            ScrollView {
                //We can set some priperties if GridItem also
                LazyVGrid(columns: [GridItem(.adaptive(minimum: 65))]) {
                    ForEach(emojis[0..<emojiCount], id: \.self) { emoji in
                        CardView(content: emoji).aspectRatio(2/3, contentMode: ContentMode.fit)
                    }
                }
            }
            .foregroundColor(.red)
            Spacer()
            HStack {
                remove
                Spacer()
                add
            }
            .font(.largeTitle)
            .padding(.horizontal)
        }
        .padding(.horizontal)
    }
    
    var add : some View {
        Button(action: {
            if emojiCount < emojis.count {
                emojiCount += 1
            }
        }, label: {
            Image(systemName: "plus.circle")
        })
    }
    
    var remove: some View {
        return Button(action: {
            if emojiCount > 1 {
                emojiCount -= 1
            }
        }, label: {
            Image(systemName: "minus.circle")
        })
    }
}

struct CardView: View {
    @State var isFaceUp: Bool = true
    var content: String
    
    var body: some View {
        ZStack {
            let shape = RoundedRectangle(cornerRadius: 20)
            if isFaceUp {
                shape.fill().foregroundColor(.white)
                shape.stroke(lineWidth: 3)
                Text(content).font(.largeTitle)
            } else {
                shape.fill()
            }
        }.onTapGesture {
            isFaceUp = !isFaceUp
        }
    }
}

